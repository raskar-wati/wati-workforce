export function getPixabot(seed: string): string {
  let h = 0
  for (let i = 0; i < seed.length; i++) {
    h = (Math.imul(31, h) + seed.charCodeAt(i)) | 0
  }
  const idx = Math.abs(h) % PIXABOT_IDS.length
  return `/pixabots/240/${PIXABOT_IDS[idx]}.png`
}

